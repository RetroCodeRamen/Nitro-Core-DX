// drawChar renders a single character at the specified pixel position directly
// into the output buffer. Used by the text MMIO registers.
func (p *PPU) drawChar(char rune, x, y int, color uint32) {
	if char >= 'a' && char <= 'z' {
		char = char - 'a' + 'A'
	}
	glyph, ok := font8x8[char]
	if !ok {
		return
	}
	for row := 0; row < 8; row++ {
		py := y + row
		if py < 0 || py >= 200 {
			continue
		}
		bits := glyph[row]
		for col := 0; col < 8; col++ {
			px := x + col
			if px < 0 || px >= 320 {
				continue
			}
			if (bits>>col)&1 != 0 {
				p.OutputBuffer[py*320+px] = color
			}
		}
	}
}

// DrawText draws text at the specified position using the 8x8 bitmap font.
func (p *PPU) DrawText(text string, x, y int, color uint32) {
	charX := x
	for _, char := range text {
		if charX+8 > 320 {
			break
		}
		p.drawChar(char, charX, y, color)
		charX += 8
	}
}
